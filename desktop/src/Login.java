        // TODO add your handling code here:
        Arquivo arquivo = new Arquivo();
        String consulta = user.getText()+ "," + senha.getText();
        try{
            boolean retorno = arquivo.pesquisar("login.txt", consulta);
            if(retorno){
                if(home == null){
                    home = new Home(user.getText());
                    home.setVisible(true);
                    this.setVisible(false);
                }
                
            }
            else{
                aviso.setText("Usu√°rio ou senha incorretos");
            }
        }
