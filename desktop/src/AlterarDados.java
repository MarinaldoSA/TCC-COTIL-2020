        if(nomeFan.getText().equals("") || slogan.getText().equals("") || descricao.getText().equals("")){
            aviso.setText("Preencha todos os campos!!");
        }
        else{
            Arquivo arquivo = new Arquivo();
            String novaLinha = nomeFan.getText()+ "," + slogan.getText() + "," + descricao.getText();
            try{
                arquivo.alteraLinha(linha, novaLinha, "empresa");
                aviso.setText("Alterado com Sucesso!!");
            }
            catch(Exception e){
                e.printStackTrace();
            }
        }
