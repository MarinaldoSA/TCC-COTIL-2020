    private void inserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserirActionPerformed
        // TODO add your handling code here:
        if(CPF.getText().equals("") || qtd.getText().equals("") || data.getText().equals("") || total.getText().equals("") || CodRef.getText().equals("")){
            aviso.setText("Preencha todos os campos!!");
        }
        else{
            try{
                
                    d.inserePedido("TCC_pedidos", CPF.getText(), total.getText(), qtd.getText(), login, data.getText(), CodRef.getText());
                    aviso.setText("Produto cadastrado!");
                    CodRef.setText("");
                    total.setText("");
                    qtd.setText("");
                    data.setText("");
                    CPF.setText("");
                    this.PopularJTable("SELECT * FROM TCC_pedidos");
            }
            catch(Exception e){
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_inserirActionPerformed

